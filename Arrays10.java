package Arraysdeclared;

public class Arrays10 {
    public static void main(String[] args) {
        String[] skill = new String[4];
        skill[0] = "Language";
        skill[1] = "Leadership";
        skill[2] = "Writing";
        skill[3] = "Communication";

        for (int i = 0; i <= skill.length; i++) {
            System.out.println(skill[i]);
        }
    }
}
